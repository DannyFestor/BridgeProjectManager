    /** @test */
    public function a_project_can_be_created()
    {
        $title = 'New Project';
        $description = 'Project Description';
        $settings = json_encode(['bg' => 'black']);

        $this
            ->actingAs($this->user)
            ->post(route('projects.store'), [
                'title' => $title,
                'description' => $description,
                'settings' => $settings,
            ])
            ->assertStatus(302)
            ->assertSessionHas('success', 'Project was created.');

        $this->assertDatabaseHas('projects', [
            'title' => $title,
            'description' => $description,
            // can't test json ... why :(
        ]);
    }

    /** @test */
    public function a_project_cannot_be_created_on_validation_error()
    {
        $title = 'New Project';
        $description = 'Project Description';
        $settings = json_encode(['bg' => 'black']);

        $this
            ->actingAs($this->user)
            ->post(route('projects.store'), [
                'title' => null,
                'description' => $description,
                'settings' => $settings,
            ])
            ->assertSessionHasErrors('title');

        $this
            ->actingAs($this->user)
            ->post(route('projects.store'), [
                'title' => str_repeat('a', 256),
                'description' => $description,
                'settings' => $settings,
            ])
            ->assertSessionHasErrors('title');

        $this
            ->actingAs($this->user)
            ->post(route('projects.store'), [
                'title' => [],
                'description' => $description,
                'settings' => $settings,
            ])
            ->assertSessionHasErrors('title');

        $this
            ->actingAs($this->user)
            ->post(route('projects.store'), [
                'title' => $title,
                'description' => null,
                'settings' => $settings,
            ])
            ->assertSessionHasErrors('description');

        $this
            ->actingAs($this->user)
            ->post(route('projects.store'), [
                'title' => $title,
                'description' => [],
                'settings' => $settings,
            ])
            ->assertSessionHasErrors('description');
    }

